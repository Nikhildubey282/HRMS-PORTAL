export const PATTERN = {
  email:/^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  password:  /^[^\s]+$/,
  // name:/(.|\s)*\S(.|\s)*/,
  name:/^([a-zA-Z]+\s)*[a-zA-Z]+$/,
  boothNumber: "^[0-9]+$",
  onlyNUmber: /^(?=.*[0-9])[-+()0-9]+$/,
  price:/(^[0][1-9]+)|([1-9]\d*)+$/,
  alphaNumeric: "^[a-zA-Z0-9]*$",
  youtubeID: /(ftp|http|https):\/\/(\w+:{0,1}\w*@)?(\S+)(:[0-9]+)?(\/|\/([\w#!:.?+=&%@!\-\/]))?/,
  colorHexCode: "^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$",
  version: /(\d+)\.(\d+)/,
  footnote: /^[0-9,]+$/,
  phone:/^(?:(?:\+{0,2})91(\s*[\-]\s*)?|[0]?)?[6789]\d{9}$/,
};
